var enames=["Rahul","Sonia","Priyanaka","Modi"];
//iterate - using [].forEach()
enames.forEach((ename)=>{
    console.log(ename)
})
//iterate - using [].map()
enames.map((ename)=>{
    console.log(ename)
})
//iterate - using [].find()
enames.find((ename)=>{
    console.log(ename)
})

//iterate - using [].filter()
enames.filter((ename)=>{
    console.log(ename)
})